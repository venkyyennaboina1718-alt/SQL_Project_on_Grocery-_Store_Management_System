CREATE  DATABASE SQL_PROJECT;
USE SQL_PROJECT;

-- 1. Supplier Table
CREATE TABLE IF NOT EXISTS supplier (
    SupplierID TINYINT PRIMARY KEY AUTO_INCREMENT,
    SupplierName VARCHAR(255),
    Address TEXT
);
SELECT * FROM supplier;

-- 2. Categories Table
CREATE TABLE IF NOT EXISTS categories (
    CategoryID TINYINT PRIMARY KEY AUTO_INCREMENT,
    CategoryName VARCHAR(255)
);
SELECT * FROM categories;

-- 3. Employees Table
CREATE TABLE IF NOT EXISTS employees (
    EmployeeID TINYINT PRIMARY KEY AUTO_INCREMENT,
    Name VARCHAR(255),
    HireDate VARCHAR(255)
);
SELECT * FROM employees;
-- 4. Customers Table
CREATE TABLE IF NOT EXISTS customers (
    CustomerID SMALLINT PRIMARY KEY AUTO_INCREMENT,
    Name VARCHAR(255),
    Address TEXT
);

select * from customers;

-- 5. Products Table
CREATE TABLE IF NOT EXISTS products (
    ProductID TINYINT PRIMARY KEY AUTO_INCREMENT,
    Name VARCHAR(255),
    SupplierID TINYINT,
    CategoryID TINYINT,
    Price DECIMAL(10,2),
    FOREIGN KEY (SupplierID) REFERENCES supplier(SupplierID)
        ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (CategoryID) REFERENCES categories(CategoryID)
        ON UPDATE CASCADE ON DELETE CASCADE
);

select * from products;

-- 6. Orders Table
CREATE TABLE IF NOT EXISTS orders (
    OrderID SMALLINT PRIMARY KEY AUTO_INCREMENT,
    CustomerID SMALLINT,
    EmployeeID TINYINT,
    OrderDate VARCHAR(255),
    FOREIGN KEY (CustomerID) REFERENCES customers(CustomerID)
        ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (EmployeeID) REFERENCES employees(EmployeeID)
        ON UPDATE CASCADE ON DELETE CASCADE
);

select * from orders;

-- 7. Order_Details Table
CREATE TABLE IF NOT EXISTS order_details (
    OrderDetailID SMALLINT PRIMARY KEY AUTO_INCREMENT,
    OrderID SMALLINT,
    ProductID TINYINT,
    Quantity TINYINT,
    PriceEach DECIMAL(10,2),
    TotalPrice DECIMAL(10,2),
    FOREIGN KEY (OrderID) REFERENCES orders(OrderID)
        ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (ProductID) REFERENCES products(ProductID)
        ON UPDATE CASCADE ON DELETE CASCADE
);

select * from order_details;

